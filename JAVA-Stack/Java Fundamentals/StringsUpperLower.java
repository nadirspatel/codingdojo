// Uppercase and Lowercase: The String class provides methods to lowercase and uppercase strings.

public class StringsUpperLower {
    public static void main(String[] args) {
    String a = "HELLO";
    String b = "world";
    System.out.println(a.toLowerCase()); // turns HELLO to hello
    System.out.println(b.toUpperCase()); // turns world to WORLD
    }
}