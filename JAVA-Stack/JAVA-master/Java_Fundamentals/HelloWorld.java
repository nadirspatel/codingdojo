public class HelloWorld{
	public static void main(String[] args){
		System.out.println("Hello World");
	}
}

// Here are a few things that you need to know:

// HelloWorld is a class. Java uses object-oriented programming for everything. In fact, it is not even possible to write a function that doesn't belong to a class. We'll dig in more with Java OOP right after dealing with data types and loops. It is important that your file name is the exactly the same as your class name.
// main is a method that defines the entry point for a exectuable Java file; it is required for any file that is intended to be run from the command line. A method is just a name for a function that belongs to a class; since everything in Java must belong to a class, all functions in Java are methods. Your methods will contain the functionality that you want your class to have.
// System.out.println("") is the bit of code that enabled us to output something to our command prompt or terminal. It is equivalent to Javascript's console.log() or Python's print().